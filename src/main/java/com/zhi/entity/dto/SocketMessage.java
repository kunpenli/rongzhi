package com.zhi.entity.dto;

public class SocketMessage {
    public String message;
    public String date;

    public String getMessage() {
        return message;
    }

    public void setMessage() {
        this.message = message;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

}
